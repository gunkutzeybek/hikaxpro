def ls(e, t):
    return (e << (t % 32)) & 0xFFFFFFFF

def extendArr(a, l):
    b = [0] * l
    for i in range(0, len(a)):
        b[i] = a[i]
    return b

def rs(e, t):
    return (e % 0x100000000) >> t

def fun0(e):
    t = ''
    for n in range(0, 4 * len(e)):
        t += '0123456789abcdef'[e[n >> 2] >> 8 * (3 - n % 4) + 4 & 15] + '0123456789abcdef'[e[n >> 2] >> 8 * (3 - n % 4) & 15]
    return t

def m(e, t):
    n = (e & 65535) + (t & 65535)
    return ls((e >> 16) + (t >> 16) + (n >> 16), 16) | n & 65535

def C(e, t):
    return rs(e, t) | ls(e, 32 - t)
 
