def fun3(e):
    e = e.replace('\r\n', '\n')
    t = ''
    for n in range(0, len(e)):
        r = ord(e[n])
        if (r < 128):
            t += chr(r)
        else:
            if r > 127 and r < 2048:
                t += chr(r >> 6 | 192)
            else:
                t += chr(r >> 12 | 224)
            t += chr(r >> 6 & 63 | 128)
            t += chr(63 & r | 128)
    return t

def fun2(e):
    t = [0] * (((8 * len(e) - 8) >> 5) + 1)
    for n in range(0, 8 * len(e), 8):
        t[n >> 5] |= ls((255 & ord(e[n//8])), 24 - n % 32)
    return t

